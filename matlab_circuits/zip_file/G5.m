function y = G5(X,s)
% buffered 60Hz notch filter
% X = [R1 R2 R3 R4 R5 C1 C2 C2];
R1=X(1);R2=X(2);R3=X(3);R4=X(4);R5=X(5);
C1=X(6);C2=X(7);C3=X(8);
A=zeros(6);
A(1,1)=1/R1+s*(C1+C2);
A(1,2)=-s*C2;A(1,5)=-1/R1;
A(2,1)=A(1,2);A(2,2)=1/R2+s*(C2+C3);
A(2,3)=-s*C3;A(2,5)=-1/R2;
A(3,2)=A(2,3);A(3,3)=1/R3+s*C3;
A(4,4)=1;A(4,5)=-1;
A(5,5)=1/R4+1/R5;A(5,6)=-1/R4;
A(6,3)=-1;A(6,6)=1;
B=[s*C1;0;1/R3;0;0;0];C=A\B;
y=20*log10(abs(C(6)));